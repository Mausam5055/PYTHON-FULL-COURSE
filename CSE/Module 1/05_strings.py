# A string is a collection of one or more characters put 
# in a single quote, double-quote or triple quote.

#EXAMPLE:

string1 = "VIT Bhopal"
string2 = "VIT Chennai"
print(type(string1))
print(type(string2))
print(string1)
print(string2)

#string Indexing 

print(string1[2])  #index 2 from left to right is 'T'
print(string1[-8]) #index -8 from right to left is 'T'

#String slicing :
print(string2[2:8]) 