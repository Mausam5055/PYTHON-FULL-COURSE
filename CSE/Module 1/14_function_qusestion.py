#Implement a function with r as a parameter and function
#will not return and value .Print the area of cirlce

def area_of_circle(r):  # 'r' is the parameter for the radius
    area = 3.14 * r ** 2  # Calculates the area of the circle
    print("The area of the circle is:", area)  # Prints the area

# Taking input from the user for the radius
n = float(input("Enter the radius: "))

# Calling the function with the user input
area_of_circle(n)



    