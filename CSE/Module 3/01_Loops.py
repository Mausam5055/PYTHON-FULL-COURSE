# In Python, there are two main types of loops:

# 1. for Loop
# Used to iterate over a sequence (list, tuple, string, etc.).

# Syntax:

# for variable in sequence:
#     # Code to execute

#EG: 

fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
# ðŸ‘‰ Prints each fruit one by one.



# 2. while Loop
# Runs as long as the condition is True.
# Syntax:

# while condition:
#     # Code to execute

# Example:


count = 1
while count <= 5:
    print(count)
    count += 1
# ðŸ‘‰ Prints numbers 1 to 5.