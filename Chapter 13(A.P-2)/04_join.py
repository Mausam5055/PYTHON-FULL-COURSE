# ---------JOIN METHOD (STRINGS)------

# Creates a string from iterable objects.
# l = ["apple", "mango", "banana"]
# result = ", and, ".join(l)
# print(result)
# The above line will return “apple,and,mango,and,banana”.

a = ["Harry", "Rohan", "Shubam"]
 
final = "-".join (a)  #this will join "dash (-)" with harry,rohan and shubam.
print(final) 

# similarly:

a = ["Harry", "Rohan", "Shubham"]

final = "::".join(a)
print(final)

