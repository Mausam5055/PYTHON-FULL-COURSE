#___FUNCTION WITH TWO ARGUMENTS___

def goodDay(name, ending):  #here arguments used is name and ending
    print("Good Day, " + name)
    print(ending)
  
goodDay("Harry","Thank You" )
goodDay("Rohan","Thank You" )
goodDay("Mausam","Thanks") 

