# ---US OF FOR LOOPS----
#WE CAN USE FOR LOOPS IN LIST,TUPLES OR STRINGS[ITERABLES]



## For Loop with Lists
l = [1, 4, 6, 234, 6, 764]
for i in l:
    print(i)

## For Loop with Tuples
t = (6, 231, 75, 122)
for i in t:
    print(i)

## For Loop with Strings
s = "Harry"
for i in s:
    print(i)  #here we will get all the alphabts printed separately 
              #this is caled ittreation of strings using for loop